package main

import ("fmt")

func main() {
  fmt.Println("---------- Loop ----------")
  Loop()

  fmt.Println("----------- Nested Loop ----------")
  NestedLoop()

  fmt.Println("---------- Continue & Break statement ----------")
  Continue()
  Break()

  fmt.Println("---------- Range -----------")
  Range()
  
  fmt.Println("---------- If Else conditions ----------")
  IfElse()

  fmt.Println("---------- Switch conditions ----------")
  SwitchSingleCase()
  SwitchMultiCase()
}
